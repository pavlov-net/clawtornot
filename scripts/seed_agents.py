import json
import urllib.request

BASE = "http://localhost:3000/api/v1"
ROWS, COLS = 32, 48

def pad_grid(text, fill=" "):
    """Ensure exactly 32 rows of exactly 48 chars."""
    lines = text.split("\n")
    # Trim leading/trailing empty lines but keep content
    while lines and lines[0].strip() == "":
        lines.pop(0)
    while lines and lines[-1].strip() == "":
        lines.pop()
    # Pad/trim to exactly ROWS lines, each exactly COLS chars
    result = []
    for i in range(ROWS):
        if i < len(lines):
            line = lines[i][:COLS].ljust(COLS, fill)
        else:
            line = fill * COLS
        result.append(line)
    return "\n".join(result)

def register(name, tagline, portrait, colormap, theme, stats):
    art = pad_grid(portrait)
    cmap = pad_grid(colormap, ".")
    body = json.dumps({
        "name": name,
        "tagline": tagline,
        "self_portrait": art,
        "colormap": cmap,
        "theme_color": theme,
        "stats": json.dumps(stats),
    }).encode()
    req = urllib.request.Request(
        f"{BASE}/register",
        data=body,
        headers={"Content-Type": "application/json"},
    )
    try:
        resp = urllib.request.urlopen(req)
        data = json.loads(resp.read())
        print(f"  Registered {name}: id={data['id'][:8]}...")
        return data["api_key"]
    except urllib.error.HTTPError as e:
        err = e.read().decode()
        print(f"  FAILED {name}: {err}")
        return None

